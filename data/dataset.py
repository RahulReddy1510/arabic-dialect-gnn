"""
data/dataset.py

PyTorch Geometric Dataset class for the Arabic dialect phoneme-graph corpus.

This wraps the pre-built ``.pt`` graph files (produced by graph_construction.py)
in a standard PyG Dataset interface. If no pre-built graphs exist, the
``process()`` method synthesizes a small demo corpus so that tests and
quick experiments can run without MADAR access.
"""

from __future__ import annotations

import logging
import random
import warnings
from pathlib import Path
from typing import Callable, List, Optional

import torch
from torch import Tensor

logger = logging.getLogger(__name__)

try:
    from torch_geometric.data import Dataset, Data  # type: ignore
    _PYG_AVAILABLE = True
except ImportError:
    warnings.warn(
        "ArabicDialectGraphDataset will not be functional. "
        "Install: pip install torch_geometric",
        UserWarning,
        stacklevel=1,
    )
    _PYG_AVAILABLE = False
    # Provide stub base classes so the module can still be imported.

    class Dataset:  # type: ignore
        def __init__(self, *args, **kwargs):
            pass

    class Data:  # type: ignore
        pass


# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

DIALECT_NAMES = ["Gulf", "Egyptian", "Levantine", "Maghrebi", "Iraqi"]
NUM_CLASSES = len(DIALECT_NAMES)
NODE_FEATURE_DIM = 9   # 8 phoneme-class one-hot + 1 position scalar

# ---------------------------------------------------------------------------
# Synthetic data generator (used when MADAR is not available)
# ---------------------------------------------------------------------------


def _make_synthetic_graph(
    label: int,
    n_nodes: Optional[int] = None,
    seed: int = 0,
) -> "Data":
    """Generate a synthetic phoneme graph with realistic size distribution.

    The synthetic graphs aren't meaningful linguistically, but they have the
    correct tensor shapes and data types, so all code that processes real
    graphs will also process these without modification.

    Args:
        label: Integer dialect label (0–4).
        n_nodes: Number of phoneme nodes. If None, drawn from U(5, 25).
        seed: RNG seed for reproducibility.

    Returns:
        A PyG Data object with ``x``, ``edge_index``, ``y``, ``num_nodes``.
    """
    rng = random.Random(seed + label * 1000)
    torch_rng = torch.Generator()
    torch_rng.manual_seed(seed + label * 1000)

    if n_nodes is None:
        n_nodes = rng.randint(5, 25)

    # Node features: one-hot phoneme class (8-dim) + position scalar (1-dim).
    x = torch.zeros((n_nodes, NODE_FEATURE_DIM), dtype=torch.float32)
    for i in range(n_nodes):
        class_idx = rng.randint(0, 7)
        x[i, class_idx] = 1.0
        x[i, 8] = i / max(n_nodes - 1, 1)   # normalized position

    # Sequential edges (bidirectional) + skip-2 edges.
    edge_srcs: List[int] = []
    edge_dsts: List[int] = []

    for i in range(n_nodes - 1):
        edge_srcs += [i, i + 1]
        edge_dsts += [i + 1, i]

    for i in range(n_nodes - 2):
        edge_srcs += [i, i + 2]
        edge_dsts += [i + 2, i]

    edge_index = torch.tensor([edge_srcs, edge_dsts], dtype=torch.long)
    y = torch.tensor(label, dtype=torch.long)

    data = Data(x=x, edge_index=edge_index, y=y, num_nodes=n_nodes)
    data.phonemes = ["ph"] * n_nodes   # placeholder phoneme labels
    return data


def _generate_synthetic_dataset(
    split: str,
    n_per_class: int = 200,
    out_dir: Path = None,
) -> None:
    """Save synthetic graphs to ``{out_dir}/{split}/`` for demo/testing.

    Args:
        split: Split name (``"train"``, ``"val"``, ``"test"``).
        n_per_class: Number of graphs per dialect class.
        out_dir: Output directory. Defaults to ``data/graphs``.
    """
    if out_dir is None:
        out_dir = Path("data/graphs")
    split_dir = out_dir / split
    split_dir.mkdir(parents=True, exist_ok=True)

    idx = 0
    for label in range(NUM_CLASSES):
        for sample in range(n_per_class):
            graph = _make_synthetic_graph(label=label, seed=idx)
            torch.save(graph, split_dir / f"{idx:06d}.pt")
            idx += 1

    logger.info(
        "Generated %d synthetic graphs for split=%s in %s",
        idx,
        split,
        split_dir,
    )


# ---------------------------------------------------------------------------
# Dataset class
# ---------------------------------------------------------------------------

class ArabicDialectGraphDataset(Dataset):
    """PyTorch Geometric Dataset for Arabic dialect phoneme graphs.

    Wraps the pre-built ``.pt`` graph files generated by
    :func:`data.graph_construction.batch_construct_graphs`.

    If no graph files exist at ``{root}/{split}/``, the ``process()`` method
    automatically generates a **synthetic demo corpus** so that tests and
    quick experiments can run without MADAR.

    Args:
        root: Root data directory. Graph files must be at ``{root}/{split}/``.
        split: One of ``"train"``, ``"val"``, ``"test"``.
        transform: Optional callable applied to each graph on load.

    Example::

        dataset = ArabicDialectGraphDataset("data/graphs", split="train")
        print(f"Graphs: {len(dataset)}, features: {dataset[0].x.shape}")
    """

    def __init__(
        self,
        root: str | Path = "data/graphs",
        split: str = "train",
        transform: Optional[Callable] = None,
    ) -> None:
        self.split = split
        self._root = Path(root)
        self._split_dir = self._root / split
        self._file_list: List[Path] = []
        self._transform = transform

        # We DON'T call super().__init__() from PyG here because PyG's Dataset
        # base class forces a specific directory structure we don't need.
        # Instead, we handle load/process manually.
        self._setup()

    def _setup(self) -> None:
        """Discover existing graph files or generate synthetic ones."""
        if self._split_dir.exists():
            self._file_list = sorted(self._split_dir.glob("*.pt"))

        if not self._file_list:
            logger.info(
                "No graph files found at %s. Running process() to generate synthetic data.",
                self._split_dir,
            )
            self.download()
            self.process()
            self._file_list = sorted(self._split_dir.glob("*.pt"))

        logger.info(
            "ArabicDialectGraphDataset(split=%s): %d graphs loaded.",
            self.split,
            len(self._file_list),
        )

    def download(self) -> None:
        """Print MADAR download instructions.

        This project uses the MADAR corpus, which requires signing a data
        usage agreement. See data/README.md for full instructions.

        If MADAR is not available, synthetic data will be used automatically.
        """
        logger.info(
            "MADAR download: visit https://camel.abudhabi.nyu.edu/madar/ "
            "to obtain the corpus. Place TSV files in data/raw/ and run "
            "python data/madar_preprocessing.py followed by "
            "python data/graph_construction.py to build graph files."
        )

    def process(self) -> None:
        """Build graph files from processed CSVs, or synthesize demo data.

        If ``data/processed/{split}.csv`` exists, runs the full graph
        construction pipeline. Otherwise generates synthetic demo graphs
        so that training and testing can proceed without real data.
        """
        csv_path = self._root.parent / "processed" / f"{self.split}.csv"

        if csv_path.exists():
            logger.info("Found processed CSV at %s. Building graphs.", csv_path)
            import pandas as pd  # type: ignore
            from data.camel_pipeline import CAMeLProcessor  # type: ignore
            from data.graph_construction import batch_construct_graphs  # type: ignore

            df = pd.read_csv(csv_path)
            processor = CAMeLProcessor()
            batch_construct_graphs(df, processor, output_dir=self._root, split=self.split)
        else:
            logger.warning(
                "No processed CSV found at %s. Generating synthetic demo corpus.",
                csv_path,
            )
            n_per_class = {"train": 200, "val": 40, "test": 40}.get(self.split, 40)
            _generate_synthetic_dataset(
                split=self.split,
                n_per_class=n_per_class,
                out_dir=self._root,
            )

    def __len__(self) -> int:
        return len(self._file_list)

    def __getitem__(self, idx: int) -> "Data":
        graph = torch.load(self._file_list[idx], weights_only=False)
        if self._transform is not None:
            graph = self._transform(graph)
        return graph

    # Keep the PyG Dataset interface compatible -----------

    def get(self, idx: int) -> "Data":
        return self.__getitem__(idx)

    def len(self) -> int:
        return self.__len__()

    def __repr__(self) -> str:
        return (
            f"ArabicDialectGraphDataset(split={self.split!r}, "
            f"n_graphs={len(self)})"
        )


# ---------------------------------------------------------------------------
# Class weight helper
# ---------------------------------------------------------------------------

def get_dialect_weights(dataset: ArabicDialectGraphDataset) -> Tensor:
    """Compute inverse-frequency class weights for weighted CrossEntropyLoss.

    Gulf Arabic is the smallest class in MADAR, so without weighting the
    model tends to trade Gulf performance for gains on larger classes.
    Inverse-frequency weighting corrects for this.

    Args:
        dataset: An :class:`ArabicDialectGraphDataset` instance.

    Returns:
        Float32 tensor of shape (NUM_CLASSES,) with per-class weights,
        normalized so the weights sum to ``NUM_CLASSES``.
    """
    counts = torch.zeros(NUM_CLASSES, dtype=torch.float32)
    for i in range(len(dataset)):
        graph = dataset.get(i)
        label = int(graph.y.item())
        counts[label] += 1.0

    # Replace zeros to avoid division by zero.
    counts = counts.clamp(min=1.0)
    weights = 1.0 / counts
    # Normalize so weights sum to NUM_CLASSES.
    weights = weights * (NUM_CLASSES / weights.sum())

    logger.info(
        "Dialect class weights (split=%s):\n  %s",
        dataset.split,
        "  ".join(
            f"{DIALECT_NAMES[i]}: {weights[i]:.3f}" for i in range(NUM_CLASSES)
        ),
    )
    return weights


